package entity;

/**
 *
 * @author Surya
 */
public class Susu {
    private int id_susu;
    private String nama;
    private int vol_liter;
    private int harga;

    public int getId_susu() {
        return id_susu;
    }

    public void setId_susu(int id_susu) {
        this.id_susu = id_susu;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getVol_liter() {
        return vol_liter;
    }

    public void setVol_liter(int vol_liter) {
        this.vol_liter = vol_liter;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

}
